let primeiro_valor = prompt("Digite o primeiro valor: ");   // Recebe o primeiro valor
let segundo_valor = prompt("Digite o segundo valor: ");     // Recebe o segundo valor

// Soma o primeiro_valor com o segundo_valor
let soma = parseInt(primeiro_valor) + parseInt(segundo_valor);
alert(`A soma dos valores é: ${soma}`);// Exibe o resultado da soma
let subtracao = parseInt(primeiro_valor) - parseInt(segundo_valor);
alert(`A subtração dos valores é: ${subtracao}`)  ; // Exibe o resultado da subtração
let multiplicacao = parseInt(primeiro_valor) * parseInt(segundo_valor);
alert (`A multiplicação dos valores é: ${multiplicacao}`) ; // Exibe o resultado da multiplicação
let divisao = parseInt(primeiro_valor) / parseInt(segundo_valor);
alert (`A divisão dos valores é: ${divisao}`) ; // Exibe o resultado da divisão